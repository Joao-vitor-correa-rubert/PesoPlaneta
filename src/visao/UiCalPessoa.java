    private void jtPesoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtPesoFocusLost
        //este comando deixa vermelho quando der erro
        if(jtPeso.getText().isEmpty()){
            jtPeso.setBackground(Color.decode("#f98000"));
        }else{
            jtPeso.setBackground(Color.white);
           //este comandop deixa verde quando acertar
            jtPeso.setForeground(Color.green);
            
        }//fecha else
    }//GEN-LAST:event_jtPesoFocusLost

    public void  calcular(){
        try {
            Pessoa p = new Pessoa();
            //if novo
            if(jtPeso.getText().isEmpty()){
                throw new NumberFormatException("peso em branco, digite novamente!");
            }else{
                p.setPeso(Integer.parseInt(jtPeso.getText()));
            }
            JOptionPane.showMessageDialog(rootPane, 
                                            "calculo bem sucedido!\n"
                                                    +p.toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane,
                                            "Numero invalido\n"
                                                    +e.getMessage());       
        }
        finally{
            System.out.println("passou aqui!");
        }
    }//fecha calcular
